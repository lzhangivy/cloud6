        <p class="information">
            Adjustment Layers in Phiotoshop guides you to help you learn about how to filter or edit your image by adding the color or tone of the image, without having to worry about editing each pixles of the original image. As you click <b>[Layer → Adjustments]</b>, it will portray sixteen various of adjustments to apply to your original image. Once the adjustments are applied to the image, there are always an option to make further changes or discard it. The Layer Properties enables you to modify more inside the adjustment layer for your image. The purpose for of Adjustment layer is to have the control of filtering and go beyond your imagination in editing your image.
