        <p class="information">
            Color is an important feature to focus on when working with Photoshop. The program, Photoshop, displays not only the basic colors, it has a color range for you to select if you are looking for a specific color. First, click on the foreground color selection box, it will appear to show the color swatch, for you to pick which color you are searching for. If you want to find out or experience more with the colors, definitely recommend to go to Adobe Color, <a href="https://color.adobe.com/create">https://color.adobe.com/create</a>, and try to find the color that you are satisfied with. 

