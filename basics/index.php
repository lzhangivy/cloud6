        Starting a new file in Photoshop is very easy, but there are a lot of important things to consider about your document when creating it. First, let's take a look at what you are presented with on the opening Photoshop screen. Taking up a majority of the page will be your recent files, allowing you quick access to any ongoing projects. You can also click the <b>[ Open ] </b> button on the left side to browse your computer's files to open up Photoshop files or images. In order to create a new project, go <b>[ File > New… ] </b> at the top left of the page; or click the <b>[ Create New… ] </b>button on the left side of the page. 

